using CookieAuthentication.Authentication;
using CookieAuthentication.Model.Abstract;
using CookieAuthentication.Model.Concrete;
using Ninject;
using Ninject.Web.Common;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using System.Web.Mvc;
using System.Web.Routing;

namespace CookieAuthentication
{
    public class NinjectControllerFactory : DefaultControllerFactory
    {
        private IKernel ninjectKernel;
        public NinjectControllerFactory()
        {
            ninjectKernel = new StandardKernel();
            AddBindings();
        }
        protected override IController GetControllerInstance(RequestContext requestContext,
       Type controllerType)
        {
            return controllerType == null
            ? null
            : (IController)ninjectKernel.Get(controllerType);
        }
        private void AddBindings()
        {
            ninjectKernel.Bind<IRepository>().To<EFRepository>();
            ninjectKernel.Bind<IAuthentication>().To<CustomAuthentication>().InRequestScope();
        }
    }
}